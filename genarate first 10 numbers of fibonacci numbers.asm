    AREA AA,CODE,READONLY
	ENTRY
	LDR R0 ,=0X00000000	;FIRST NUMBER
	LDR R1 ,=0X00000001  ;SECOND NUMBER
	LDR R2 ,=0X00000009	;COUNTER
	LDR R3 ,=0X40000000	;ADRESS
	STR R0,[R3],#4
	BL FBS
STOP B STOP

FBS	STR R1,[R3],#4
	MOV R4,R1
	ADD R1,R1,R0
	MOV R0,R4
	SUBS R2,R2,#0X01
	BNE FBS
	MOV PC,LR
	END